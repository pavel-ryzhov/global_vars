class Symbol
{
public:
	int real_h;
	int real_w;
	int content[H][W];

	Symbol()
	{
		set_empty();
	}
	void set_empty()
	{
		for (int i = 0; i < H; i++)
		{
			for (int j = 0; j < W; j++)
			{
				content[i][j] = 0;
			}
		}
	}
	void set_content(int c[H][W])
	{
		for (int i = 0; i < H; i++)
		{
			for (int j = 0; j < W; j++)
			{
				content[i][j] = c[i][j];
			}
		}
	}
	void getData()
	{
		this->set_empty();
		this->real_h = 0;
		this->real_w = 0;
		char buffer[64];
		string s = "";
		int i = -1;
		do
		{
			cin.getline(buffer, sizeof(buffer) - 1);
			s = buffer;
			i++;
			this->real_h++;
			if (real_w == 0)
				this->real_w = s.length();
			for (int j = 0; j < (int)s.length(); j++)
			{
				int x = 0;
				if (s[j] == '.')
					x = 0;
				else
					x = 1;
				/*else if (s[j] == '%')
					x = 1;
				else if (s[j] == '#')
					x = 2;
				else if (s[j] == '@')
					x = 3;*/
				this->content[i][j] = x;
			}
		} 
		while (s.length() > 0);
	}
	void show()
	{
		for (int i = 0; i < real_h; i++)
		{
			for (int j = 0; j < real_w; j++)
			{
				cout << content[i][j];
			}
			cout << endl;
		}
	}
	void resize()
	{
		int tmp[H][W];
		for (int i = 0; i < H; i++)
		{
			for (int j = 0; j < W; j++)
			{
				tmp[i][j] = 0;
			}
		}
		for (int i = 0; i < H; i++)
		{			
			for (int j = 0; j < W; j++)
			{
				tmp[i][j] = content[int((i * real_h)/ H)][j];
			}
		}

		int tmp_2[H][W];
		for (int i = 0; i < H; i++)
		{
			for (int j = 0; j < W; j++)
			{
				tmp_2[i][j] = 0;
			}
		}
		for (int j = 0; j < W; j++)
		{			
			for (int i = 0; i < H; i++)
			{
				tmp_2[i][j] = tmp[i][int((j * real_w) / W)];
			}
		}

		for (int i = 0; i < H; i++)
			for (int j = 0; j < W; j++)
				this->content[i][j] = tmp_2[i][j];

		real_h = H;
		real_w = W;
	}
	void truncate()
	{
		int left = W;
		int right = 0;
		int top = H;
		int bottom = 0;
		for (int i = 0; i < real_h; i++)
		{
			for (int j = 0; j < real_w; j++)
			{
				if (content[i][j] != 0)
				{
					if (i < top)
						top = i;
					if (i > bottom)
						bottom = i;
					if (j < left)
						left = j;
					if (j > right)
						right = j;
				}
			}
		}
		int tmp[H][W];
		for (int i = 0; i < H; i++)
		{
			for (int j = 0; j < W; j++)
			{
				tmp[i][j] = 0;
			}
		}
		for (int i = top; i <= bottom; i++)
		{
			for (int j = left; j <= right; j++)
			{
				tmp[i - top][j - left] = content[i][j];
			}
		}
		set_empty();
		set_content(tmp);
		real_h = bottom - top + 1;
		real_w = right - left + 1;
	}
	void normalize()
	{
		truncate();
		resize();
	}
	int r_hamming(Symbol s)
	{
		int r = 0;
		for (int i = 0; i < H; i++)
		{
			for (int j = 0; j < W; j++)
			{
				if (content[i][j] - s.content[i][j] > 0)
					r+= content[i][j] - s.content[i][j]; 
				else
					r+= s.content[i][j] - content[i][j];
			}
		}
		return r;
	}
};

