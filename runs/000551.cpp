{
	fstream inputFile(inputFileName);
	fstream outputFile(outputFileName);
	int h = 0;
	while (!inputFile.eof())
	{
		string s = "";
		char buffer[64];
		inputFile.getline(buffer, sizeof(buffer) - 1);
		s = buffer;
		string tmp = "";
		h++;
		for (int i = 0; i < (int)s.length(); i++)
		{
			char symbol;
			if (s[i] == '.')
				symbol = '0';
			else if (s[i] == '%')
				symbol = '1';
			else if (s[i] == '#')
				symbol = '2';
			else if (s[i] == '@')
				symbol = '3';
			tmp = tmp + symbol + ", ";
		}
		for (int i = s.length(); i < LOAD_W; i++)
			tmp = tmp + "0, ";
		tmp = tmp.substr(0, tmp.length() - 2);
		tmp = "{" + tmp + "},";
		tmp += '\n';
		outputFile.write(tmp.data(), tmp.length());
	}
	for (int i = h; h < LOAD_H; h++)
	{
		string tmp = "";
		for (int j = 0; j < LOAD_W; j++)
			tmp = tmp + "0, ";
		tmp = tmp.substr(0, tmp.length() - 2);
		tmp = "{" + tmp + "},";
		tmp += '\n';
		outputFile.write(tmp.data(), tmp.length());
	}
	outputFile.close();
	inputFile.close();

	return 0;
}

