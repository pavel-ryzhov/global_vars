int recognize()
{
	int flag = 0;
	for (int i = 0; i < digit.size(); i++)
	{
		if (!flag)
		{
			flag = 1;
			for (int j = 0; j < digit[0].length(); j++)
				if (digit[i][j] != '.')
					flag = 0;
		}
		else
		{
			for (int j = 0; j < digit[0].length(); j++)
				if (digit[i][j] != '.')
				{
					digit.erase(digit.begin(), digit.begin()+i);
					flag = 0;
					break;
				}
			if (!flag)
				break;
		}
	}
	
	flag = 0;
	for (int i = digit.size()-1; i >= 0; i--)
	{
		if (!flag)
		{
			flag = 1;
			for (int j = 0; j < digit[0].length(); j++)
				if (digit[i][j] != '.')
					flag = 0;
		}
		else
		{
			for (int j = 0; j < digit[0].length(); j++)
				if (digit[i][j] != '.')
				{
					digit.erase(digit.end()-digit.size()+i+1, digit.end());
					flag = 0;
					break;
				}
			if (!flag)
				break;
		}
	}
	
	int left;
	for (left = 0; left < digit[0].length(); left++)
	{
		flag = 0;
		for (int i = 0; i < digit.size(); i++)
			if (digit[i][left] != '.')
			{
				flag = 1;
				break;
			}
		if (flag)
			break;
	}
	
	int right;
	for (right = left+1; right < digit[0].length(); right++)
	{
		flag = 1;
		for (int i = 0; i < digit.size(); i++)
			if (digit[i][right] != '.')
			{
				flag = 0;
				break;
			}
		if (flag)
			break;
	}
	
	for (int i = 0; i < digit.size(); i++)
	{
		for (int j = 0; j < digit[i].length()-left; j++)
			digit[i][j] = digit[i][j+left];
		digit[i].resize(right-left);
	}
	
	for (int i = 0; i < 12; i++)
		hits[i] = 0;
	
	double kx = (double) SAMPLE_WIDTH/(double) digit[0].length();
	double ky = (double) SAMPLE_HEIGHT/(double) digit.size();
	for (int i = 0; i < digit.size(); i++)
		for (int j = 0; j < digit[0].length(); j++)
			for (int n = 0; n < 12; n++)
			{
				if (digit[i][j] != '.' && samples[n][(int)(i*ky)][(int)(j*kx)] == '@')
					hits[n]++;
				else if (samples[n][(int)(i*ky)][(int)(j*kx)] == '@')
					hits[n]--;
			}
			
	int max = hits[0], max_index = 0;
	for (int i = 1; i < 12; i++)
		if (hits[i] > max)
		{
			max = hits[i];
			max_index = i;
		}
	return max_index%10;
}

