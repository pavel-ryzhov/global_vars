void read_input(vector<string>&matrix)
{
	string tmp;
	int status=-1,l,count;
	cin>>tmp;
	l=tmp.size();
	matrix.push_back(tmp);
	while(true)
	{
		count=0;
		cin>>tmp;
		matrix.push_back(tmp);
		for(int i=0;i<l;++i)
		{
			if(status==-1 && tmp[i]!='.')
			{
				status=0;
			}
			else if(status==0 && tmp[i]=='.')
				count++;
		}
		if(count==l)
			return;
	}
}

int separate_v(vector<string>&matrix,vector<int>&seps_v)
{
	int h=matrix.size();
	int w=matrix[0].size();
	int*dens=new int[w];
	int i,j,status=0,parts=0,s,sorted,tmp;
	vector<int> locmins;
	vector<int>::iterator it;

	for(i=0;i<w;++i)
	{
		dens[i]=0;
		for(j=0;j<h;++j)
		{
			if(matrix[j][i]=='@')
			{
				dens[i]=-1;
				break;
			}
			if(matrix[j][i]=='#')
				dens[i]+=DARK_GREY;
			else if(matrix[j][i]=='%')
				dens[i]+=LIGHT_GREY;
		}
		if(dens[i]==0 && status==1)
		{
			status=0;
			seps_v.push_back(i);
		}
		else if(dens[i]!=0 && status==0)
		{
			status=1;
			parts++;
			seps_v.push_back(i);
		}
	}

	if(dens[w-1]!=0)
		seps_v.push_back(w);

	if(parts==3)
		return 3;
	for(i=1;i<w-1;++i)
	{
		if(dens[i]==dens[i-1])
			continue;
		if(dens[i]>0 && dens[i]<MAXSPACE && (dens[i-1]>dens[i] || dens[i-1]<0) && (dens[i+1]>=dens[i] || dens[i+1]<0))
			locmins.push_back(i);
	}
	s=locmins.size();
	if(s==0)
		return parts;
	for(i=0;i<s;++i)
	{
		sorted=1;
		for(j=s-1;j>i;--j)
		{
			if(dens[locmins[j-1]]>dens[locmins[j]])
			{
				tmp=locmins[j-1];
				locmins[j-1]=locmins[j];
				locmins[j]=tmp;
				sorted=0;
			}
			if(sorted==1)
				break;
		}
	}

	tmp=0;
	for(i=0;i<3-parts && i<s;++i)
	{
		it=seps_v.begin();
		j=0;
		while(seps_v[j]<locmins[i])
		{
			++it;
			++j;
		}
		seps_v.insert(it,2,locmins[i]+1);
		++tmp;
	}
	if(dens[w-1]!=0)
		seps_v.push_back(w-1);
	
	return parts+tmp;
}

