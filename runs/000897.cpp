int okrug(double c, int up)
{
	int ret=(int)c;
	if(up==1&&(double)ret<c)ret++;
	return ret;
}
double getSize(double koeff,int i,int it)
{
	double start=koeff*i,finish=koeff*(i+1);
	if(start<=(double)it) start=(double)it;
	if(finish>=(double)(it+1)) finish=(double)(it+1);
	if(finish<=start)return 0.;
	return (finish-start);
}
int getWeight(int sym) {
	switch(sym)
	{
		case '@': return BLACK;
		case '#': return DARK_GREY;
		case '%': return LIGHT_GREY;
	}
	return 0;
}
int getSym(double koeff)
{
	if(koeff>=((double)(BLACK+DARK_GREY))/2.) return '@';
	if(koeff>=((double)(LIGHT_GREY+DARK_GREY))/2.) return '#';
	if(koeff>=((double)(LIGHT_GREY))/2.) return '%';
	return '.';
}
void resize(struct symbol *sym)
{
	int i,j,h=sym->h,w=sym->w,x1=0,x2=sym->w-1,y1=0,y2=sym->h-1;
	for(i=0;i<h;++i)
	{
		for(j=0;j<w;++j)
			if(sym->array[i*w+j]!='.')
				break;
		if(j==w)
			y1++;
		else
			break;
	}
	for(i=h-1;i>-1;--i)
	{
		for(j=0;j<w;++j)
			if(sym->array[i*w+j]!='.')
				break;
		if(j==w)
			y2--;
		else
			break;
	}
	for(i=0;i<w;++i)
	{
		for(j=0;j<h;++j)
			if(sym->array[j*w+i]!='.')
				break;
		if(j==h)
			x1++;
		else
			break;
	}
	for(i=w-1;i>-1;--i)
	{
		for(j=0;j<h;++j)
			if(sym->array[j*w+i]!='.')
				break;
		if(j==h)
			x2--;
		else
			break;
	}
	double koeff=(double)(y2-y1+1)/20.,sizex,sizey,result;
	w=okrug(((1./koeff)* (double)(x2-x1+1)),1);
	int *arr=new int[20*w];
	int kx1,kx2,ky1,ky2,itx,ity;
	for(i=0;i<w;++i)
	{
		for(j=0;j<20;++j)
		{
			kx1=okrug(i*koeff,0)+x1;
			kx2=okrug((i+1)*koeff,1)+x1;
			ky1=okrug(j*koeff,0)+y1;
			ky2=okrug((j+1)*koeff,1)+y1;
			result=0.;
			for(ity=ky1;ity<ky2;ity++)
			{
				sizey=getSize(koeff,j,ity-y1);
				for(itx=kx1;itx<kx2;itx++)
				{
					sizex=getSize(koeff,i,itx-x1);
					result+=sizex*sizey*getWeight(sym->array[ity*sym->w+itx]);
				}
			}
			arr[j*w+i]=getSym(result/(koeff*koeff));
			
		}
	}
	for(i=0;i<w;++i)
	{
		for(j=0;j<20;++j)
		{
			sym->array[j*w+i]=arr[j*w+i];
		}
	}
	sym->w=w;
	sym->h=20;
}

int getInt(char c) {
	switch(c) {
	case '@': return 3;
	case '#': return 2;
	case '%': return 1;
	}
	return 0;
}

