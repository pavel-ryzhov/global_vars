int main()
{
	vector<string> matrix;
	int i,j,k,parts_v=0,parts_i=0,status,h,w,n,wm;
	struct symbol symbols[3];
	vector<int> seps_v,seps_i;

	try
	{
		read_input(matrix);
		wm=matrix[0].size();
		parts_v=separate_v(matrix,seps_v);
		if(parts_v<3)
			parts_i=separate_i(matrix,seps_i);
		if(parts_i<parts_v)
			for(i=0;i<parts_v;++i)
			{
				symbols[i].h=h=matrix.size();
				symbols[i].w=w=seps_v[2*i+1]-seps_v[2*i];
				symbols[i].array=new int[h*w];
				for(j=0;j<h;++j)
					for(k=0;k<w;++k)
						(symbols[i].array)[j*w+k]=matrix[j][seps_v[2*i]+k];
						
				resize(&symbols[i]);
				recognize(symbols[i]);
			}
		else
			for(i=0;i<parts_i;++i)
			{
				symbols[i].h=h=matrix.size();
				symbols[i].w=w=seps_i[2*i+1]-seps_i[2*i]+h/2;

				symbols[i].array=new int[h*w];
				for(j=0;j<h;++j)
					for(k=0;k<w;++k)
					{
						n=k-(h-j)/2;
						if(n<0 || seps_i[2*i]+k-h/2 <0)
							(symbols[i].array)[j*w+k]='.';
						else if(n>(w-h/2) || (seps_i[2*i]+k-h/2) >= wm)
							(symbols[i].array)[j*w+k]='.';
						else
							(symbols[i].array)[j*w+k]=matrix[j][seps_i[2*i]+k-h/2];
					}
				resize(&symbols[i]);
				recognize(symbols[i]);
			}
	}
	catch(bad_alloc)
	{
		printf("Not enough memory\n");
		return 0;
	}

	return 0;
}