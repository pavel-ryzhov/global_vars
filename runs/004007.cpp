template<class T> class DataMatrix
{
protected:
	deque< deque<T> > matrix;
public:
	DataMatrix(int height, int width)
	{
		matrix.resize(height);
		for (int i = 0; i < height; i++)
			matrix[i].resize(width);
	}

	DataMatrix()
	{
	}

	DataMatrix(const T *dataArray, int height, int width)
	{
		matrix.resize(height);
		for (int i = 0; i < height; i++)
			matrix[i].resize(width);
		for (int i = 0; i < height; i++)
		{
			for (int j = 0; j < width; j++)
			{
				matrix[i][j] = dataArray[width * i + j];
			}
		}
	}

	int Width() const
	{
		if (!matrix.empty())
			return matrix[0].size();
		else
			return 0;
	}

	int Height() const
	{
		return matrix.size();
	}

	int Square() const
	{
		return Height() * Width();
	}

	int Count(T element) const
	{
		int count = 0;
		for (int i = 0; i < Height(); i++)
		{
			for (int j = 0; j < Width(); j++)
			{
				if (matrix[i][j] == element)
					count += 1;
			}
		}
		return count;
	}

	int NonZeroCount() const
	{
		int count = 0;
		for (int i = 0; i < Height(); i++)
		{
			for (int j = 0; j < Width(); j++)
			{
				if (matrix[i][j] != T(0))
					count += 1;
			}
		}
		return count;
	}

	double Weight(T element) const
	{
		double weight = double(Count(element)) / double(Square());
		return weight;
	}

	T &operator() (const int i, const int j)
	{
		return matrix[i][j];
	}

	DataMatrix GetSubmatrix(const RectCoordinats &coord, T mask) const
	{
		DataMatrix<T> submatrix(coord.GetHeight(), coord.GetWidth());
		for (int i = 0; i < coord.GetHeight(); i++)
			for (int j = 0; j < coord.GetWidth(); j++)
			{
				T matrixValue = this->matrix[coord.GetBottom() + i][coord.GetLeft() + j];
				if (matrixValue == mask)
					submatrix(i, j) = T(1);
				else
					submatrix(i, j) = T(0);
			}
		return submatrix;
	}

	double GetExpectedValue(const RectCoordinats &coord) const
	{
		double expectedValue = 0;

		double left = coord.GetLeft();
		double right = coord.GetRight();
		double bottom = coord.GetBottom();
		double top = coord.GetTop();


		double leftCoefficient = int(left) + 1 - left;
		double rightCoefficient = right - int(right);
		double bottomCoefficient = int(bottom) + 1 - bottom;
		double topCoefficient = top - int(top); 

		for (int i = int(bottom); i < int(top) + 1; i++)
		{
			for (int j = left; j < int(right) + 1; j++)
			{
				double coefficient = 1;
				if (i == int(bottom))
				{
					if (i != int(top))
						coefficient *= bottomCoefficient;
					else
						coefficient *= top - bottom;
				}
				else
				{
					if (i == int(top))
					{
						if (topCoefficient != 0)
							coefficient *= topCoefficient;
						else
							continue;
					}
				}

				if (j == int(left))
				{
					if (j != int(right))
						coefficient *= leftCoefficient;
					else
						coefficient = right - left;
				}
				else
				{
					if (j == int(right))
					{
						if (rightCoefficient != 0)
							coefficient *= rightCoefficient;
						else
							continue;
					}
				}

				expectedValue += this->matrix[i][j] * coefficient;
			}
		}
		
		expectedValue /= coord.GetSquare();
		return expectedValue;
	}
};

