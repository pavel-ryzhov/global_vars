void Picture::Read()
{
	deque<double> currentLine;
	bool isReading = false;

	char charLine[MAX_PICTURE_WIDTH] = {0};

	while (!isReading)
	{
		currentLine.clear();
		cin.getline(charLine, MAX_PICTURE_WIDTH);
		int i = 0;
		while (charLine[i] != 0)
		{
			double pixelColor = getColor(charLine[i]);
			currentLine.push_back(pixelColor);
			if (pixelColor != White)
				isReading = true;
			i++;
		}
	}

	while (isReading)
	{
		this->matrix.push_back(currentLine);
		currentLine.clear();
		bool lineIsWhite = true;
		cin.getline(charLine, MAX_PICTURE_WIDTH);
		int i = 0;
		while (charLine[i] != 0)
		{
			double pixelColor = getColor(charLine[i]);
			currentLine.push_back(pixelColor);
			if (pixelColor != White)
				lineIsWhite = false;
			i++;
		}
		if (lineIsWhite)
			isReading = false;
	}

	int left = 0;
	int right = Width() - 1;

	bool lineIsWhite = true;
	while (lineIsWhite)
	{
		for (int j = 0; j < Height(); j++)
		{
			if (this->matrix[j][left] != White)
				lineIsWhite = false;
		}
		if (lineIsWhite)
			left++;
	}

	lineIsWhite = true;
	while (lineIsWhite)
	{
		for (int i = 0; i < Height(); i++)
		{
			if (this->matrix[i][right] != White)
				lineIsWhite = false;
		}
		if (lineIsWhite)
			right--;
	}

	int width = Width();
	for (int i = 0; i < Height(); i++)
	{
		for (int j = 0; j < left; j++)
			this->matrix[i].pop_front();
		for (int j = 0; j < width - right - 1; j++)
			this->matrix[i].pop_back();
	}
}

double Picture::getColor(char pixel)
{
	switch (pixel)
	{
	case Picture::ASCII_Black:

	case Picture::ASCII_DarkGray:
		return Black;

	case Picture::ASCII_Gray:
		return Gray;

	case Picture::ASCII_White:
		return White;
	}
}

Picture Picture::Resize(int height, int width)
{
	Picture newPicture(height, width);

	double oldWidth = this->Width();
	double oldHeight = this->Height();
	double newWidth = width;
	double newHeight = height;
	double newBottom;
	double newLeft;
	double newTop;
	double newRight;

	for (int i = 0; i < height; i++)
		for (int j = 0; j < width; j++)
		{
			newBottom = i;
			newLeft = j;
			newRight = newLeft + 1;
			newTop = newBottom + 1;

			double left = newLeft * oldWidth / newWidth;
			double right = newRight * oldWidth / newWidth;
			double bottom = newBottom * oldHeight / newHeight;
			double top  = newTop * oldHeight / newHeight;
			RectCoordinats oldPicturePart(left, right, bottom, top);
			
			double expectedValue = this->GetExpectedValue(oldPicturePart);
			if (expectedValue < 0.5)
				newPicture(i,j) = 0;
			else 
				newPicture(i,j) = 1;
		}
	return newPicture;
}

bool Picture::IsDefinitelyNotADigit() const
{
	double weight = this->Weight(1);
	double height = this->Height();
	double width = this->Width();
	double heightToWidth = height / width;

	if (weight < 0.1 || width < 3 || height < 6 || heightToWidth > 20 || heightToWidth < 0.05)
		return true;
	return false;
}

Picture Picture::Difference(Picture picture1, Picture picture2)
{
	Picture difference(picture1.Height(), picture1.Width());

	for (int i = 0; i < picture1.Height(); i++)
		for (int j = 0; j < picture1.Width(); j++)
		{
			difference(i,j) = picture1(i,j) - picture2(i,j);
		}
	
	return difference;
}

