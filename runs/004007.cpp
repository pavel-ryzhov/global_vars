char CompareEngine::MatchingDigit(Picture picture)
{
	double currentError;
	double resultError;
	int resultDigit = 0;
	int currentDigit = 0;
	
	int pictureSquare = picture.Square();
	for (currentDigit = 0; currentDigit < 10; currentDigit++)
	{
		Picture &pattern = this->patternVector[currentDigit];
		int patternSquare = pattern.Square();
		
		Picture difference;
		if (patternSquare > pictureSquare)
		{
			Picture resizedPattern = pattern.Resize(picture.Height(), picture.Width());
			difference = Picture::Difference(picture, resizedPattern);
		}
		else
		{
			Picture resizedPicture = picture.Resize(pattern.Height(), pattern.Width());
			difference = Picture::Difference(resizedPicture, pattern);
		}

		currentError = double(difference.NonZeroCount()) / double(difference.Square());
		if (currentDigit != 0)
		{
			if (currentError < resultError)
			{
				resultError = currentError;
				resultDigit = currentDigit;
			}
		}
		else
		{
			resultError = currentError;
		}
	}

	return '0' + resultDigit;
}

