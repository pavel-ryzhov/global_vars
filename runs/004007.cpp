RectCoordinats::RectCoordinats()
{
	this->left = -1;
	this->right = -1;
	this->top = -1;
	this->bottom = -1;
}

RectCoordinats::RectCoordinats(double left, double right, double bottom, double top)
{
	this->left = left;
	this->right = right;
	this->bottom = bottom;
	this->top = top;
}

void RectCoordinats::AddPoint(int x, int y)
{
	if (x < bottom || bottom == -1)
		this->bottom = x;
	if (x > top || top == -1)
		this->top = x + 1;
	if (y < left || left == -1)
		this->left = y;
	if (y > right || right == -1)
		this->right = y + 1;
}

double RectCoordinats::GetLeft() const
{
	return this->left;
}

double RectCoordinats::GetBottom() const
{
	return this->bottom;
}

double RectCoordinats::GetRight() const
{
	return this->right;
}

double RectCoordinats::GetTop() const
{
	return this->top;
}

double RectCoordinats::GetHeight() const
{
	return this->top - this->bottom;
}

double RectCoordinats::GetWidth() const
{
	return this->right - this->left;
}

double RectCoordinats::GetSquare() const
{
	return this->GetHeight() * this->GetWidth();
}


