vector<vector<string> > divideintoparts(vector<string>&pic1)
{
	vector<vector<string> > pic2;
	bool meetblack = true, meetblackprev = false;
	int iter = 0;
	int pos = 0,len;
	for(int i = 0; i < pic1[0].size(); i++)
	{
		if(meetblack) meetblackprev = true;
		else meetblackprev = false;
		meetblack = false;
		for(int j = 0; j < pic1.size(); j ++)
		{
			if(pic1[j][i]=='@'||pic1[j][i]=='#')
			{
				meetblack = true;
				break;
			}
		}
		if(meetblack==false && meetblackprev == true)
		{
			iter++;
			if(iter > 3) break;
			pic2.resize(iter);
			pic2[iter-1].resize(pic1.size());
			len = i - pos;
			for(int j = 0; j < pic1.size(); j ++)
			{
				pic2[iter-1][j] = pic1[j].substr(pos,len);
			}
			pos = i;
		}
	}
	return pic2;
}
vector<string> scaleletters(vector<string>&pic1)
{
	vector<string>pic = pic1;

	clearbottom(pic);
	cleartop(pic);
	clearright(pic);
	clearleft(pic);

	if(pic.size() < IDEAL_Y)
		return pic;
	double scale;
	scale = pic[0].size() / 10.0;
	double avr ,amp ,coef;
	char temp[IDEAL_X];
	for(int y = 0;y < pic.size(); y++)
	{
		for(int x = 0; x< IDEAL_X; x++)
		{
			avr = 0;
			for(int i = 0; i < pic[y].size(); i++)
			{
				if (pic[y][i] == '.')
					amp = 0;
				else if (pic[y][i] == '@')
					amp = 1;
				
				coef = (x+1)*scale - i;                
				if(coef < 0 || coef > scale+1)
					coef = 0;
				else if(coef > scale)
					coef = i+1-scale*x;
				else if(coef > 1)
					coef = 1;
				avr += amp * coef;
	
			}
			avr = avr / scale;
			if(avr < 0.5)
				temp[x] = '.';
			else if(avr >= 0.5)
				temp[x] = '@';
		}
		pic[y].assign(temp);
		pic[y].resize(10);
	}
	char temp1[IDEAL_Y];
	scale = pic.size() / 15.0;
	for(int x = 0; x< IDEAL_X; x++)
	{
		for(int y = 0;y < IDEAL_Y; y++)
		{
			avr = 0;
			for(int i = 0; i < pic.size(); i++)
			{
				if (pic[i][x] == '.')
					amp = 0;
				else if (pic[i][x] == '@')
					amp = 1;
				
				coef = (y+1)*scale - i;                
				if(coef < 0 || coef > scale+1)
					coef = 0;
				else if(coef > scale)
					coef = i+1-scale*y;
				else if(coef > 1)
					coef = 1;
				avr += amp * coef;
			}
			avr = avr / scale;
			if(avr < 0.5)
				temp1[y] = '.';
			else if(avr >= 0.5)
				temp1[y] = '@';
		}
		for(int i = 0; i < IDEAL_Y; i ++)
			pic[i][x] = temp1[i];
	}
	pic.resize(IDEAL_Y);
	return pic;
}

