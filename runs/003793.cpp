int check_range(vector<vector<char> > &v, double ci, double cj1, double cj2, bool weak) {
	int probability[10], i, k;
	for(i = 0; i < 10; probability[i++] = 0);

	bool perceptron[10];

	for(double cj = cj1 ; cj > cj2; cj -= 0.1) {
		check(v, ci, cj, perceptron);
		k = count_true(perceptron, i, probability, weak);
		if(k == 1 && !weak) { probability[i]++; /*cout << (i + 1) % 10;*/ }
	}
	int max = 0;
	for(int i = 1; i < 10; i++) {
		if(probability[max] < probability[i]) max = i;
	}
	if(!probability[max]) return -1;
	return (max + 1) % 10;
}

int recognise(vector<vector<char> > &v) {
	if(holes(v) == 2) return 8;
	double ci, cj, c, eps;
	ci = (double(A)) / v.size();
	cj = (double(A)) / v[0].size();
	c = min(ci, cj);

	eps = 0.1;
	int res;

	while(true) {
		res = check_range(v, ci, min(c + eps, cj), max(c - eps, 0.0), false);
		if(res >= 0 || (c + eps > cj && c - eps < 0.0)) break;
		else eps += 0.1;
	}

	if(res < 0) return check_range(v, ci, c, c/2, true);
	return res >= 0 ? res : 0;
}

