double T(vector<char> &b) {
	int t = 0, f = 0, i;
	for(i = 0; i < (int)b.size(); i++)
		if(b[i] - '0') t++; else f++;
	return (double)t / (t + f);
}

rect fill(int i, int j, vector<vector<char> > &v, char c) {
	if(!inside(i, j, v) || c == v[i][j]) return rect();
	char old_c = v[i][j];
	stack<pair<int, int> > s;
	rect r;
	s.push(pair<int, int>(i, j));
	while(!s.empty()) {
		i = s.top().first, j = s.top().second;
		v[i][j] = c;
		resize(r, i, j);
		s.pop();
		if(inside(i - 1, j - 1, v) && v[i - 1][j - 1] == old_c) s.push(pair<int, int>(i - 1, j - 1));
		if(inside(i - 1, j, v) && v[i - 1][j] == old_c) s.push(pair<int, int>(i - 1, j));
		if(inside(i - 1, j + 1, v) && v[i - 1][j + 1] == old_c) s.push(pair<int, int>(i - 1, j + 1));
		if(inside(i, j - 1, v) && v[i][j - 1] == old_c) s.push(pair<int, int>(i, j - 1));
		if(inside(i, j + 1, v) && v[i][j + 1] == old_c) s.push(pair<int, int>(i, j + 1));
		if(inside(i + 1, j - 1, v) && v[i + 1][j - 1] == old_c) s.push(pair<int, int>(i + 1, j - 1));
		if(inside(i + 1, j, v) && v[i + 1][j] == old_c) s.push(pair<int, int>(i + 1, j));
		if(inside(i + 1, j + 1, v) && v[i + 1][j + 1] == old_c) s.push(pair<int, int>(i + 1, j + 1));
	}
	return r;
}

void framed_copy(vector<vector<char> > &v, vector<vector<char> > &u) {
	u.push_back(vector<char>(v[0].size() + 2, '0'));
	for(int i = 0; i < (int)v.size(); i++) {
		u.push_back(vector<char>());
		u.back().push_back('0');
		for(int j = 0; j < (int)v[i].size(); j++)
			u.back().push_back(v[i][j]);
		u.back().push_back('0');
	}
	u.push_back(vector<char>(v[0].size() + 2, '0'));
}

bool find(vector<vector<char> > &v, char c, int &i, int &j) {
	for(int i1 = 0; i1 < (int)v.size(); i1++) {
		for(int j1 = 0; j1 < (int)v[i1].size(); j1++) {
			if(v[i1][j1] == c) {
				i = i1; j = j1;
				return true;
			}
		}
	}
	return false;
}

