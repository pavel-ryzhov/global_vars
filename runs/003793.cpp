struct rect {
    int left, right, top, bot;
    rect() : left(-1), right(-1), top(-1), bot(-1) { ; }
};

void resize(rect &r, int i, int j) {
    if(j < r.left || r.left == -1) r.left = j;
    if(j > r.right || r.right == -1) r.right = j;
    if(i < r.top || r.top == -1) r.top = i;
    if(i > r.bot || r.bot == -1) r.bot = i;
}

bool inside(int i, int j, vector<vector<char> > &v) {
	return (i >= 0 && j >= 0) && ((int)v.size() > i && (int)v[i].size() > j);
}

rect search(int i, int j, vector<vector<char> > &v, vector<vector<char> > &u) {
	if(!inside(i, j, v)) return rect();
	char old_c = v[i][j];
	stack<pair<int, int> > s;
	rect r;
	s.push(pair<int, int>(i, j));
	while(!s.empty()) {
		i = s.top().first, j = s.top().second;
		v[i][j] = '0';
		u[i][j] = '1';
		resize(r, i, j);
		s.pop();
		if(inside(i - 1, j - 1, v) && v[i - 1][j - 1] == old_c) s.push(pair<int, int>(i - 1, j - 1));
		if(inside(i - 1, j, v) && v[i - 1][j] == old_c) s.push(pair<int, int>(i - 1, j));
		if(inside(i - 1, j + 1, v) && v[i - 1][j + 1] == old_c) s.push(pair<int, int>(i - 1, j + 1));
		if(inside(i, j - 1, v) && v[i][j - 1] == old_c) s.push(pair<int, int>(i, j - 1));
		if(inside(i, j + 1, v) && v[i][j + 1] == old_c) s.push(pair<int, int>(i, j + 1));
		if(inside(i + 1, j - 1, v) && v[i + 1][j - 1] == old_c) s.push(pair<int, int>(i + 1, j - 1));
		if(inside(i + 1, j, v) && v[i + 1][j] == old_c) s.push(pair<int, int>(i + 1, j));
		if(inside(i + 1, j + 1, v) && v[i + 1][j + 1] == old_c) s.push(pair<int, int>(i + 1, j + 1));
	}
	return r;
}

void cut(vector<vector<char> > &v, rect &r, vector<vector<char> > &u) {
	int i, j;
	for(i = r.top; i <= r.bot; i++) {
		u.push_back(vector<char>());
		for(j = r.left; j <= r.right; j++) {
			u.back().push_back(v[i][j]);
			v[i][j] = '0';
		}
	}
}

void isolate(vector<vector<char> > &pixs, vector<vector<vector<char> > > &connected_regions) {
	int i, j;
	vector<vector<char> > connected_region;
	for(i = 0; i < (int)pixs.size(); i++)
		connected_region.push_back(vector<char>(pixs[0].size(), '0'));

	for(j = 0; j < (int)pixs[0].size(); j++) {
		for(i = 0; i < (int)pixs.size(); i++) {
			if(pixs[i][j] == '1') {
				rect r = search(i, j, pixs, connected_region);
				vector<vector<char> > u;
				cut(connected_region, r, u);
				connected_regions.push_back(u);
			}
		}
	}
}

void transf(vector<vector<char> > &v, vector<vector<char> > &u, double ci, double cj) {
	int i, j, i1, j1, i2, j2, i3, j3;
	for(i = 0; i < (int)v.size(); i++) {
		for(j = 0; j < (int)v[i].size(); j++) {

			i2 = floor(i * ci), j2 = floor(j * cj);
			i3 = floor((i + 1) * ci), j3 = floor((j + 1) * cj);

			for(i1 = i2; i1 < i3; i1++) {
				for(j1 = j2; j1 < j3; j1++) {
					u[i1][j1] = v[i][j];
				}
			}
		}
	}
}

void matrix_to_vector(vector<vector<char> > &v, vector<char> &l) {
	int k = 0;
	for(int i = 0; i < (int)v.size(); i++)
		for(int j = 0; j < (int)v[i].size(); j++)
			l[k++] = v[i][j];
}

